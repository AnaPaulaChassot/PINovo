    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
       j.setTitulo(txtTitulo.getText());
       j.setDescricao(txtOBS.getText());
       j.setTipo(txtTipo.getText());
       j.setMemoria(Integer.parseInt(txtMemoria.getText()));
       j.setPreco(Double.parseDouble(txtPreco.getText()));
       j.setNumDias(Integer.parseInt(txtDias.getText()));
       j.setCategoria((categoria)cmbCategoria.getSelectedItem());
       
       if (j.getId() == 0){
           inserir();
       }else{
           alterar();
       }
    }//GEN-LAST:event_btnGravarActionPerformed


      public cadastroJogos(listaJogos lista) {
        initComponents();
        this.j = new jogo(new categoria());
        this.lista = lista;
        for (categoria c : dao_categoria.listarPorTipo('J')){
        cmbCategoria.addItem(String.valueOf(c.getTipo())); //Tem que transformar o char e pegar o tipo
        }
    }
      
      public void setJogo (jogo j){
          this.j=j;
          txtTitulo.setText(j.getTitulo());
          txtOBS.setText(j.getDescricao());
          cmbCategoria.setSelectedItem(j.getCategoria());
          txtTipo.setText(j.getTipo());
          txtPreco.setText(String.valueOf(j.getPreco()));
          txtMemoria.setText(String.valueOf(j.getMemoria()));
          txtDias.setText(String.valueOf(j.getNumDias()));
      }
      
      private void inserir() {
        if (dao_jogo.inserir(j)){
            JOptionPane.showMessageDialog(this, "Jogo inserido com sucesso!");
            lista.listarJogos();
            dispose();
        }else {
            JOptionPane.showMessageDialog(this, "Erro ao inserir jogo!");
        }
    }
    private void alterar(){
        if (dao_jogo.alterar(j)){
            JOptionPane.showMessageDialog(this, "Jogo alterado com sucesso!");
            lista.listarJogos();
            dispose();
        }else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar jogo!");
        }
    }



